class CreateVisualizationInput(BaseModel):
    """Input for the create_visualization tool."""
    vega_lite_spec: str = Field(
        description=(
            "A COMPLETE Vega-Lite v5 JSON specification as a string. "
            "Must include $schema, data.values (the actual data rows), "
            "mark, encoding, and a descriptive title. "
            "Use 'container' for width."
        )
    )


@tool("create_visualization", args_schema=CreateVisualizationInput)
def create_visualization(vega_lite_spec: str) -> str:
    """Add a new chart tile to the user's dashboard.

    Provide a COMPLETE Vega-Lite v5 JSON spec with the data already
    embedded in data.values. The chart will appear as a new tile on the
    dashboard.
    """
    try:
        spec = _parse_llm_json(vega_lite_spec)
    except json.JSONDecodeError as e:
        return json.dumps({"error": f"Invalid JSON: {e}"})

    if "$schema" not in spec:
        spec["$schema"] = "https://vega.github.io/schema/vega-lite/v5.json"

    tile_id = str(uuid.uuid4())

    logger.info(
        "[TOOL:create_visualization] tile=%s  mark=%s  rows=%d",
        tile_id,
        spec.get("mark", "?"),
        len((spec.get("data") or {}).get("values", [])),
    )

    return json.dumps({
        "action": "create_visualization",
        "tile_id": tile_id,
        "vega_spec": spec,
    })


