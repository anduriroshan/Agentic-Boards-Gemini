class GetBigQuerySchemaInput(BaseModel):
    """Input for the get_bigquery_schema tool."""
    table_name: str = Field(description="The fully-qualified BigQuery table name (project.dataset.table).")


@tool("get_bigquery_schema", args_schema=GetBigQuerySchemaInput)
def get_bigquery_schema(table_name: str) -> str:
    """Get the column definitions (schema) for a specific BigQuery table.
    Use this if you know a table exists (e.g. from search_metadata) but don't know its columns.
    """
    from src.bigquery.client import get_bigquery_manager
    bq = get_bigquery_manager()
    
    # Sanitize the table name - it might come in with backticks or dots
    clean_table = table_name.strip("`")
    parts = clean_table.split(".")
    if len(parts) < 3:
        return f"Error: '{table_name}' is not a fully-qualified BigQuery table name (project.dataset.table)."
    
    project_id, dataset_id, table_id = parts[0], parts[1], parts[2]
    
    # In BigQuery standard SQL, INFORMATION_SCHEMA is scoped to the dataset
    sql = f"""
    SELECT column_name, data_type 
    FROM `{project_id}`.{dataset_id}.INFORMATION_SCHEMA.COLUMNS 
    WHERE table_name = '{table_id}'
    """
    
    try:
        rows = bq.query(sql)
        if not rows:
            return f"No schema found for table '{table_name}'. Verify the table exists."
        return _safe_json_dumps(rows, indent=2)
    except Exception as e:
        return f"Error fetching schema: {str(e)}"


@tool("execute_bigquery", args_schema=ExecuteBigQueryInput)
def execute_bigquery(sql: str) -> str:
    """Execute a SQL query on Google BigQuery and return the result rows.
    
    Use this for BigQuery tables (e.g. agentic-boards.iowa_liquor_retail_sales.sales).
    Standard SQL syntax is required.
    """
    from src.bigquery.client import get_bigquery_manager
    bq = get_bigquery_manager()
    
    try:
        logger.info("[TOOL:execute_bigquery] %s", sql[:200])
        pdf = bq.query_pandas(sql)
        rows = pdf.to_dict(orient="records")
        return _safe_json_dumps({"rows": rows, "row_count": len(rows)})
    except Exception as e:
        logger.error("[TOOL:execute_bigquery] failed: %s", e)
        return _safe_json_dumps({"error": str(e), "rows": []})


@tool("execute_sql", args_schema=ExecuteSQLInput)
def execute_sql(sql: str) -> str:
    """Execute a SQL query on the Databricks warehouse and return the
    result rows as a JSON array.

    Call search_metadata first to discover available tables and columns,
    then write a SQL query targeting those tables.
    """
    # ── SQL injection / write-operation guard ─────────────────────────────────
    from src.databricks.sql_validator import validate_sql_read_only, InvalidSQLError
    try:
        validate_sql_read_only(sql)
    except InvalidSQLError as e:
        return json.dumps({
            "error": str(e),
            "rows": [],
        })
    # ── End guard ─────────────────────────────────────────────────────────────

    from src.databricks.client import get_databricks_manager

    dm = get_databricks_manager()

    if not dm.is_connected:
        logger.warning("[TOOL:execute_sql] Databricks not connected")
        return json.dumps({"error": "Databricks is not connected.", "rows": []})

    try:
        import math
        import pandas as _pd

        logger.info("[TOOL:execute_sql] %s", sql[:200])
        pdf = dm.query_pandas(sql)
        rows = pdf.to_dict(orient="records")
        logger.info("[TOOL:execute_sql] returned %d rows", len(rows))

        # ── Post-query data-quality check ─────────────────────────────────
        # Detect when every numeric column is all-null or all-zero — this
        # almost always means the query hit a "No Data" period.  Alert the
        # agent so it re-queries instead of charting meaningless zeroes.
        warning: str | None = None
        if rows:
            num_cols = [
                c for c in pdf.columns
                if _pd.api.types.is_numeric_dtype(pdf[c])
            ]
            if num_cols:
                all_empty = all(
                    pdf[c].apply(
                        lambda v: v is None or (isinstance(v, float) and (math.isnan(v) or v == 0.0)) or v == 0
                    ).all()
                    for c in num_cols
                )
                if all_empty:
                    warning = (
                        "DATA QUALITY WARNING: all numeric columns are zero or null. "
                        "This period likely contains no real data. "
                        "Do NOT visualise this result. "
                        "Re-run the query targeting a different period that has actual non-zero values. "
                        "Do NOT apply COALESCE or replace nulls with 0."
                    )
                    logger.warning("[TOOL:execute_sql] all-zero/null result — likely a 'No Data' period")

        result: dict = {"rows": rows, "row_count": len(rows)}
        if warning:
            result["warning"] = warning
        return _safe_json_dumps(result)
    except Exception as e:
        err_str = str(e)
        logger.error("[TOOL:execute_sql] failed: %s", err_str)
        # Surface a concise, actionable message for view schema errors
        if "INCOMPATIBLE_VIEW_SCHEMA_CHANGE" in err_str:
            table_hint = sql.split("FROM")[-1].split()[0] if "FROM" in sql.upper() else "unknown"
            return _safe_json_dumps({
                "error": (
                    f"INCOMPATIBLE_VIEW_SCHEMA_CHANGE: The VIEW '{table_hint}' has a stale schema. "
                    "Do NOT retry with this view. Re-run the query using the underlying base table directly "
                    "(e.g. gold_variancesummary instead of vw_variance_with_hierarchies)."
                ),
                "rows": [],
            })
        return _safe_json_dumps({"error": err_str, "rows": []})


