def _safe_json_dumps(obj: object, **kwargs) -> str:
    """json.dumps that handles dates, decimals, NaN/Inf floats, and other non-serializable types."""
    import math

    class _Encoder(json.JSONEncoder):
        def default(self, o: object) -> object:
            if isinstance(o, (datetime.date, datetime.datetime)):
                return o.isoformat()
            if isinstance(o, decimal.Decimal):
                return float(o)
            if isinstance(o, (set, frozenset)):
                return list(o)
            return super().default(o)

        def iterencode(self, o: object, _one_shot: bool = False):
            # Walk the structure and replace nan/inf floats with None
            # before encoding so they become JSON null instead of NaN/Infinity
            # (which are not valid JSON and break downstream parsing).
            return super().iterencode(_sanitize(o), _one_shot)

    def _sanitize(v: object) -> object:
        if isinstance(v, float) and (math.isnan(v) or math.isinf(v)):
            return None
        if isinstance(v, dict):
            return {k: _sanitize(val) for k, val in v.items()}
        if isinstance(v, (list, tuple)):
            return [_sanitize(i) for i in v]
        return v

    return json.dumps(obj, cls=_Encoder, **kwargs)

