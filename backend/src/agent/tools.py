class ModifyDashboardInput(BaseModel):
    """Input for the modify_dashboard tool."""
    modifications: str | dict[str, Any] | list[dict[str, Any]] = Field(
        description=(
            'A JSON object (or JSON string of that object) with any combination of these optional keys: '
            '"spec_updates" (array of {tile_id, vega_spec}) to change chart appearance; '
            '"layout_updates" (array of {tile_id, x, y, w, h}) to reposition/resize on the 12-column grid; '
            '"title_updates" (array of {tile_id, title}) to rename the tile header label; '
            '"kpi_updates" (array of {tile_id, value, subtitle, color}) to modify metric cards; '
            '"text_updates" (array of {tile_id, markdown}) to update the content of a text/markdown tile. '
            'Each row ≈ 100px. w goes from 1–12 (12=full width).'
        )
    )


@tool("modify_dashboard", args_schema=ModifyDashboardInput)
def modify_dashboard(modifications: str | dict[str, Any] | list[Any]) -> str:
    """Modify existing dashboard tiles — change chart specs (colours, marks,
    axis labels), rename tile header labels, and/or reposition / resize tiles
    on the 12-column grid.

    Use title_updates to rename the tile's header label (the banner shown above
    the chart/table). This is SEPARATE from the Vega-Lite chart title inside
    the visualisation itself.

    Layout coordinates:
    - x: column (0-11), left-to-right
    - y: row (0+), top-to-bottom
    - w: width in columns (1-12, 12=full width, 6=half)
    - h: height in row units (each row ≈ 100px, typical chart h=3-5)
    """
    if isinstance(modifications, str):
        try:
            mods = _parse_llm_json(modifications)
        except json.JSONDecodeError as e:
            return json.dumps({"error": f"Invalid JSON: {e}"})
    elif isinstance(modifications, (dict, list)):
        mods = modifications
    else:
        return json.dumps({"error": "Invalid modifications payload type."})

    # The live model may occasionally return a top-level JSON array instead of the
    # expected object. Normalize common list shapes so we can recover instead of
    # throwing and killing the live session.
    if isinstance(mods, list):
        if not mods:
            mods = {}
        else:
            normalized_items: list[dict] = []
            for item in mods:
                if isinstance(item, str):
                    try:
                        parsed = _parse_llm_json(item)
                        if isinstance(parsed, dict):
                            normalized_items.append(parsed)
                    except json.JSONDecodeError:
                        logger.warning("[TOOL:modify_dashboard] ignored non-JSON list item: %r", item[:120])
                elif isinstance(item, dict):
                    normalized_items.append(item)

            if not normalized_items:
                mods = {}
            else:
                bucketed: dict[str, list[dict]] = {
                    "spec_updates": [],
                    "layout_updates": [],
                    "title_updates": [],
                    "kpi_updates": [],
                    "text_updates": [],
                }
                for item in normalized_items:
                    tile_id = item.get("tile_id")

                    if "spec_updates" in item:
                        spec_val = item.get("spec_updates")
                        if isinstance(spec_val, list):
                            bucketed["spec_updates"].extend([u for u in spec_val if isinstance(u, dict)])
                        elif isinstance(spec_val, dict):
                            spec_upd = dict(spec_val)
                            if tile_id and "tile_id" not in spec_upd:
                                spec_upd["tile_id"] = tile_id
                            bucketed["spec_updates"].append(spec_upd)
                        continue

                    if "layout_updates" in item:
                        layout_val = item.get("layout_updates")
                        if isinstance(layout_val, list):
                            bucketed["layout_updates"].extend([u for u in layout_val if isinstance(u, dict)])
                        elif isinstance(layout_val, dict):
                            layout_upd = dict(layout_val)
                            if tile_id and "tile_id" not in layout_upd:
                                layout_upd["tile_id"] = tile_id
                            bucketed["layout_updates"].append(layout_upd)
                        continue

                    if "title_updates" in item:
                        title_val = item.get("title_updates")
                        if isinstance(title_val, list):
                            bucketed["title_updates"].extend([u for u in title_val if isinstance(u, dict)])
                        elif isinstance(title_val, dict):
                            title_upd = dict(title_val)
                            if tile_id and "tile_id" not in title_upd:
                                title_upd["tile_id"] = tile_id
                            bucketed["title_updates"].append(title_upd)
                        continue

                    if "kpi_updates" in item:
                        kpi_val = item.get("kpi_updates")
                        if isinstance(kpi_val, list):
                            bucketed["kpi_updates"].extend([u for u in kpi_val if isinstance(u, dict)])
                        elif isinstance(kpi_val, dict):
                            kpi_upd = dict(kpi_val)
                            if tile_id and "tile_id" not in kpi_upd:
                                kpi_upd["tile_id"] = tile_id
                            bucketed["kpi_updates"].append(kpi_upd)
                        continue

                    if "text_updates" in item:
                        text_val = item.get("text_updates")
                        if isinstance(text_val, list):
                            bucketed["text_updates"].extend([u for u in text_val if isinstance(u, dict)])
                        elif isinstance(text_val, dict):
                            text_upd = dict(text_val)
                            if tile_id and "tile_id" not in text_upd:
                                text_upd["tile_id"] = tile_id
                            bucketed["text_updates"].append(text_upd)
                        continue

                    if "vega_spec" in item:
                        bucketed["spec_updates"].append(item)
                    elif all(k in item for k in ("x", "y", "w", "h")):
                        bucketed["layout_updates"].append(item)
                    elif "markdown" in item:
                        bucketed["text_updates"].append(item)
                    elif any(k in item for k in ("value", "subtitle", "color")):
                        bucketed["kpi_updates"].append(item)
                    elif "title" in item:
                        bucketed["title_updates"].append(item)

                mods = {k: v for k, v in bucketed.items() if v}

    if not isinstance(mods, dict):
        return json.dumps({"error": "Invalid modifications payload. Expected an object or list of update objects."})

    def _as_update_list(value: object) -> list[dict[str, Any]]:
        if isinstance(value, list):
            return [v for v in value if isinstance(v, dict)]
        if isinstance(value, dict):
            return [value]
        return []

    def _with_tile_id(update: dict[str, Any]) -> dict[str, Any]:
        tile_id = update.get("tile_id") or update.get("tileId")
        if tile_id and "tile_id" not in update:
            return {**update, "tile_id": tile_id}
        return update

    result: dict = {"action": "modify_dashboard"}

    spec_updates = _as_update_list(mods.get("spec_updates") or mods.get("specUpdates"))
    layout_updates = _as_update_list(mods.get("layout_updates") or mods.get("layoutUpdates"))
    title_updates = _as_update_list(mods.get("title_updates") or mods.get("titleUpdates"))
    kpi_updates = _as_update_list(mods.get("kpi_updates") or mods.get("kpiUpdates"))
    text_updates = _as_update_list(mods.get("text_updates") or mods.get("textUpdates"))

    if spec_updates:
        # Normalise each spec_update: parse vega_spec if the LLM sent it as a
        # JSON string instead of an object, and inject $schema if missing.
        normalised_spec_updates = []
        for su in spec_updates:
            su = _with_tile_id(su)
            vs = su.get("vega_spec") or su.get("vegaSpec") or su.get("spec")
            if vs is None:
                # Some model outputs place patch keys directly on the update
                # object (alongside tile_id) instead of nesting under vega_spec.
                # Treat the remaining keys as a partial Vega-Lite patch.
                direct_patch = {
                    k: v
                    for k, v in su.items()
                    if k not in ("tile_id", "tileId", "vega_spec", "vegaSpec", "spec")
                }
                if direct_patch:
                    vs = direct_patch
            if isinstance(vs, str):
                try:
                    vs = _parse_llm_json(vs)
                except json.JSONDecodeError as e:
                    return json.dumps({"error": f"Invalid vega_spec JSON in spec_updates: {e}"})
            if isinstance(vs, dict) and "$schema" not in vs and any(k in vs for k in ("mark", "encoding", "layer", "data")):
                vs["$schema"] = "https://vega.github.io/schema/vega-lite/v5.json"
            normalised_spec_updates.append({**su, "vega_spec": vs})
        
        if normalised_spec_updates:
            result["spec_updates"] = normalised_spec_updates
            logger.info(
                "[TOOL:modify_dashboard] spec updates for tile(s): %s",
                [u.get("tile_id") for u in normalised_spec_updates],
            )

    if layout_updates:
        valid_layout_updates = [_with_tile_id(u) for u in layout_updates]
        if valid_layout_updates:
            result["layout_updates"] = valid_layout_updates
            logger.info(
                "[TOOL:modify_dashboard] layout updates for tile(s): %s",
                [u.get("tile_id") for u in valid_layout_updates],
            )

    if title_updates:
        valid_title_updates = [_with_tile_id(u) for u in title_updates]
        if valid_title_updates:
            result["title_updates"] = valid_title_updates
            logger.info(
                "[TOOL:modify_dashboard] title updates for tile(s): %s",
                [u.get("tile_id") for u in valid_title_updates],
            )

    if kpi_updates:
        valid_kpi_updates = [_with_tile_id(u) for u in kpi_updates]
        if valid_kpi_updates:
            result["kpi_updates"] = valid_kpi_updates
            logger.info(
                "[TOOL:modify_dashboard] kpi updates for tile(s): %s",
                [u.get("tile_id") for u in valid_kpi_updates],
            )

    if text_updates:
        valid_text_updates = [_with_tile_id(u) for u in text_updates]
        if valid_text_updates:
            result["text_updates"] = valid_text_updates
            logger.info(
                "[TOOL:modify_dashboard] text updates for tile(s): %s",
                [u.get("tile_id") for u in valid_text_updates],
            )

    if not any(k in result for k in ("spec_updates", "layout_updates", "title_updates", "kpi_updates", "text_updates")):
        result["error"] = "No spec_updates, layout_updates, title_updates, kpi_updates, or text_updates provided."

    return json.dumps(result)


